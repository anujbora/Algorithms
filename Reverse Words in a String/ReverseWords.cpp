#include <algorithm>
#include <vector>
#include <string>
using namespace std;

/**
 *  Time Complexity = O(n)
 */

void reverseword(string &s, int i, int j)
{
    while (i < j)
    {
        char t = s[i];
        s[i++] = s[j];
        s[j--] = t;
    } 
}
    
void reverseWords(string &s) 
{    
    int i = 0;
    int j = 0;
    int l = 0;
    int len = s.length();
    int wordcount = 0;
    
    while(true)
    {
        while (i<len && s[i] == ' ') 
        {
            i++;
        }
        
        if (i == len)
        {
            break;
        }

        if (wordcount) 
        {
            s[j++] = ' ';
        }

        l = j;
        
        while (i < len && s[i] != ' ') 
        {
            s[j] = s[i];
            j++;
            i++;
        }

        reverseword(s,l,j-1);
        wordcount++;  
    }
    
    s.resize(j);
    reverseword(s,0,j-1);
}